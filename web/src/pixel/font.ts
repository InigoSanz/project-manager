/** Glifo para caracteres no soportados: caja hueca. */
const UNKNOWN = [0b11111, 0b10001, 0b10001, 0b10001, 0b10001, 0b10001, 0b11111];

export const GLYPH_W = 5;
export const GLYPH_H = 7;
const TRACKING = 1; // columna de aire entre glifos

/** Ancho en px (a escala 1) que ocupará `text`. */
export function measurePixelText(text: string): number {
  const n = text.length;
  return n === 0 ? 0 : n * GLYPH_W + (n - 1) * TRACKING;
}

/**
 * Dibuja `text` (se pasa a mayúsculas) con la esquina superior izquierda en (x,y).
 * Coordenadas en píxeles del contexto actual; usar la transform de cámara para escalar.
 */
export function drawPixelText(
  ctx: CanvasRenderingContext2D,
  text: string,
  x: number,
  y: number,
  color: string,
  scale = 1,
): void {
  ctx.fillStyle = color;
  let cx = x;
  for (const ch of text.toUpperCase()) {
    const rows = G[ch] ?? UNKNOWN;
    for (let ry = 0; ry < GLYPH_H; ry++) {
      const row = rows[ry];
      if (row === 0) continue;
      for (let rx = 0; rx < GLYPH_W; rx++) {
        if (row & (1 << (GLYPH_W - 1 - rx))) {
          ctx.fillRect(cx + rx * scale, y + ry * scale, scale, scale);
        }
      }
    }
    cx += (GLYPH_W + TRACKING) * scale;
  }
}
